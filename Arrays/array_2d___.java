package Arrays;

public class array_2d___ {
    public static void main(String[] args) {
        int [] arr ={1,2,3};


        for(int item:arr ){
            System.out.println(item);
        }

        System.out.println("---");

        for(int i=0;i<arr.length;i++){
            System.out.println(arr[i]);
        }
    }
}
