package JAVA_PROGRAMS_04_09_2024.Apna_college_Practice_set;

public class Square_pattern {
    public static void main(String[] args) {
        int n =4;
        int m =5;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=m;j++){
                System.out.print("* ");
            }
            System.out.println();

        }
    }
}
