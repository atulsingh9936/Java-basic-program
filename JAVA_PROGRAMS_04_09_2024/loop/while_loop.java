package JAVA_PROGRAMS_04_09_2024.loop;

public class while_loop {
    public static void main(String[] args) {
        // while
        //A -> Init
        //B -> Condition
        // c -> Increment

        int i=1;  //A
        while(i<=10){
            System.out.println(i);//B
            // execute the code
            i++; //C
        }
    }
}
