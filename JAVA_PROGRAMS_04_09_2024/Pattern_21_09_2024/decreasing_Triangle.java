package JAVA_PROGRAMS_04_09_2024.Pattern_21_09_2024;

public class decreasing_Triangle {
    public static void main(String[] args) {
        int n=5;
        for(int i=1;i<=n;i++){
            for(int j=i;j<=n;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
